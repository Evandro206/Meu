def sum(numMemori, num):
    numMemori += num
    return  numMemori

def sub(numMemori, num):
    numMemori -= num
    return  numMemori
    
def multipli(numMemori, num):
    numMemori = numMemori * num
    return  numMemori

def division(numMemori, num):
    numMemori = numMemori / num
    return  numMemori

def Exponential(numMemori, num):
    numMemori = numMemori ** num
    return  numMemori

def root(numMemori, num):
    numMemori = numMemori ** (1/num)
    return numMemori